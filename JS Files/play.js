document.getElementById('a1').addEventListener('click', function() {
    if(document.getElementById('threegames').style.display == 'inline-block'){
        document.getElementById('threegames').style.display = 'none';
    } else {
        document.getElementById('threegames').style.display = 'inline-block';
    }
});

